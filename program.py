# para criar o intervalo de 1 a 100 pode-se utilizar o comando "range" iniciando em 1
#(continuação da outra linha) ja que o inicio é exato e terminando em 101 
#(continuação) pois o fim é sempre um numero antes do valor indicado.

soma = 0 
for x in range(1, 101):
    soma += x 
print (soma)
print('Fim do programa')